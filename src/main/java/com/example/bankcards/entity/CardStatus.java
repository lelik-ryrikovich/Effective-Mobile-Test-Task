package com.example.bankcards.entity;

/**
 * Статусы банковской карты.
 */
public enum CardStatus {

    /**
     * Карта активна и доступна для операций.
     */
    ACTIVE,

    /**
     * Карта заблокирована администратором или системой.
     */
    BLOCKED,

    /**
     * Срок действия карты истёк.
     */
    EXPIRED,

    /**
     * Ожидается блокировка карты по запросу пользователя.
     */
    PENDING_BLOCK
}
